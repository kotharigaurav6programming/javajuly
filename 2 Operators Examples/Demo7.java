// Basic Program on operator

class Demo7{
    public static void main(String args[]){
       int a = 5;
       System.out.println("a++ : "+a++);     // 5 ---> 6
       System.out.println("++a : "+(++a));   // 7
       System.out.println("a-- : "+a--);     // 7 ---> 6
       System.out.println("--a : "+(--a));   // 5
    }
}